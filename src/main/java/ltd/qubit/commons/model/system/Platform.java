package ltd.qubit.commons.model;

/**
 * The enumeration of platforms.
 *
 * @author Haixing Hu
 */
public enum Platform {

  /**
   * Apple iOS system.
   */
  IOS,

  /**
   * Apple iPadOS system.
   */
  IPAD_OS,

  /**
   * Google Android system.
   */
  ANDROID,

  /**
   * Microsoft Windows Phone system.
   */
  WINDOWS_PHONE,

  /**
   * Microsoft Windows desktop system.
   */
  WINDOWS,

  /**
   * Linux desktop system.
   */
  LINUX,

  /**
   * Mac desktop system.
   */
  MAC,

  /**
   * Web page.
   */
  WEB,

  /**
   * Unknown operating system.
   */
  UNKNOWN
}
